#!/bin/bash
# ===========================================
# 智课工坊 - Vercel 部署脚本
# ===========================================

set -e

echo "=========================================="
echo "  智课工坊 - Vercel 快速部署"
echo "=========================================="
echo ""

# 检查pnpm
if ! command -v pnpm &> /dev/null; then
    echo "📦 安装 pnpm..."
    npm install -g pnpm
fi

# 安装依赖
echo "📦 安装依赖..."
pnpm install

# 构建
echo "🔨 构建项目..."
pnpm build

# 部署到预览
echo "🚀 部署到预览环境..."
vercel

echo ""
echo "=========================================="
echo "  部署完成！"
echo "=========================================="
